#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	if (!(*alst) && new)
	{
		*alst = new;
		return ;
	}
	else if (!new)
		return ;
	new->next = *alst;
	(*alst) = new;
}
